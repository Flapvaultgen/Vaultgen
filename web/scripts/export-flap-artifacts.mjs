/**
 * Embeds on-chain artifact bytecodes for browser wallet deploys (CodegenVaultFactory).
 * Run: node web/scripts/export-flap-artifacts.mjs  (after forge build)
 */
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
const factoryArtifact = path.join(repoRoot, "out/CodegenVaultFactory.sol/CodegenVaultFactory.json");
const outPath = path.join(repoRoot, "web/src/lib/flap-artifacts.ts");

function readBytecode(artifactPath, label) {
  let json;
  try {
    json = JSON.parse(readFileSync(artifactPath, "utf8"));
  } catch {
    console.error(`Missing ${label} artifact at ${artifactPath}. Run \`forge build\` first.`);
    process.exit(1);
  }
  const bytecode = json?.bytecode?.object;
  if (!bytecode?.startsWith("0x")) {
    console.error(`Invalid bytecode in ${label} artifact.`);
    process.exit(1);
  }
  return bytecode;
}

const factoryBytecode = readBytecode(factoryArtifact, "CodegenVaultFactory");

writeFileSync(
  outPath,
  `/** Auto-generated by web/scripts/export-flap-artifacts.mjs — do not edit. */
export const CODEGEN_VAULT_FACTORY_CREATION_CODE =
  "${factoryBytecode}" as const;
`
);

console.log(`Wrote ${outPath} (factory ${(factoryBytecode.length - 2) / 2} bytes)`);
